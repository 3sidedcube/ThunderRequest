import Foundation
import CryptoKit

public extension String {
    
    /// Returns HEX md5 string of self
    var md5Hex: String? {
        let digest = Insecure.MD5.hash(data: self.data(using: .utf8) ?? Data())

        return digest.map {
            String(format: "%02hhx", $0)
        }.joined()
    }
}
